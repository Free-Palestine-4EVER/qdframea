// Get current language
function getCurrentLang() {
    return localStorage.getItem('eurovista-lang') || 'en';
}

// Set language
function setLanguage(lang) {
    localStorage.setItem('eurovista-lang', lang);
    applyTranslations(lang);
    updateLangSwitcher(lang);
    document.documentElement.lang = lang;
}

// Apply translations to elements with data-i18n attribute
function applyTranslations(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[key] && translations[key][lang]) {
            el.textContent = translations[key][lang];
        }
    });

    // Also handle placeholder translations
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[key] && translations[key][lang]) {
            el.placeholder = translations[key][lang];
        }
    });
}

// Update switcher UI
function updateLangSwitcher(lang) {
    const currentBtn = document.getElementById('langCurrent');
    if (currentBtn && languageNames[lang]) {
        currentBtn.innerHTML = `${languageNames[lang].flag} ${lang.toUpperCase()} <svg width="10" height="10" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="3" stroke-linecap="round" stroke-linejoin="round"><path d="M6 9l6 6 6-6"/></svg>`;
    }

    // Mark active language
    document.querySelectorAll('.lang-option').forEach(opt => {
        opt.classList.toggle('active', opt.dataset.lang === lang);
    });
}

// Initialize language switcher
function initLangSwitcher() {
    const switcher = document.getElementById('langSwitcher');
    if (!switcher) return;

    const currentBtn = document.getElementById('langCurrent');
    const dropdown = document.getElementById('langDropdown');

    // Toggle dropdown
    currentBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        dropdown.classList.toggle('open');
    });

    // Language option click
    dropdown.querySelectorAll('.lang-option').forEach(opt => {
        opt.addEventListener('click', () => {
            setLanguage(opt.dataset.lang);
            dropdown.classList.remove('open');
        });
    });

    // Close dropdown on outside click
    document.addEventListener('click', () => {
        dropdown.classList.remove('open');
    });

    // Apply current language
    const lang = getCurrentLang();
    setLanguage(lang);
}

// Init on DOM ready
document.addEventListener('DOMContentLoaded', initLangSwitcher);
